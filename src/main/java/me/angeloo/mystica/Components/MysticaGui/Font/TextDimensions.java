package me.angeloo.mystica.Components.MysticaGui.Font;

public class TextDimensions {

    private final int width;

    private final int height;

    public TextDimensions(
            int width,
            int height
    ) {

        this.width = width;
        this.height = height;
    }

    public int width() {
        return width;
    }

    public int height() {
        return height;
    }

}
